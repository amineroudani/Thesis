# Function to find the number of minimas for a given polynomial
# The number of minimas is inferred from the count of positive b solutions, divided by 2
function find_minimas_for_poly(P)
    # Differentiate the polynomial with respect to each variable
    grad_P = [differentiate(P, var) for var in [A_1, A_2, b_1, b_2]]
    
    # Solve the system of equations obtained from setting the gradient to zero
    result = solve(grad_P)
    
    # Define an epsilon value to consider a solution as practically real
    epsilon = 1e-2
    
    # Extract all solutions from the result
    all_solutions = solutions(result)
    
    # Filter solutions to those that are practically real, based on epsilon
    practically_real_solutions = filter(solution -> all(val -> abs(imag(val)) < epsilon, solution), all_solutions)
    
    # Further filter to retain only solutions where b_1 and b_2 are positive
    positive_b_solutions = filter(solution -> real(solution[3]) > 0 && real(solution[4]) > 0, practically_real_solutions)
    
    # Return the number of these solutions divided by 2, as our estimate of minimas
    return length(positive_b_solutions) / 2
end

